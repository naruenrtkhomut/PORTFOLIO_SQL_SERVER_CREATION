	CREATE TABLE TB_PRT_UEDU_TABLE (
		CL_ID NVARCHAR(15) NOT NULL PRIMARY KEY,
		CL_EDUCATION NVARCHAR(15) NOT NULL,
		CL_COURSE NVARCHAR(MAX) NOT NULL,
